public class CocktailDetails {
  private String mId;
  private String mThumb;
  private String mName;
  private String mCategory;
  private String mAlcoholic;
  private String mGlass;
  private String mInstructions;

  public String getmId() {
    return mId;
  }

  public void setmId(String mId) {
    this.mId = mId;
  }

  public String getmThumb() {
    return mThumb;
  }

  public void setmThumb(String mThumb) {
    this.mThumb = mThumb;
  }

  public String getmName() {
    return mName;
  }

  public void setmName(String mName) {
    this.mName = mName;
  }

  public String getmCategory() {
    return mCategory;
  }

  public void setmCategory(String mCategory) {
    this.mCategory = mCategory;
  }

  public String getmGlass() {
    return mGlass;
  }

  public void setmGlass(String mGlass) {
    this.mGlass = mGlass;
  }

  public String getmAlcoholic() {
    return mAlcoholic;
  }

  public void setmAlcoholic(String mAlcoholic) {
    this.mAlcoholic = mAlcoholic;
  }

  public String getmInstructions() {
    return mInstructions;
  }

  public void setmInstructions(String mInstructions) {
    this.mInstructions = mInstructions;
  }


}
